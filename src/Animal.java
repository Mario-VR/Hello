public class Animal {
    private String specie;

    public String getSpecie(){
        return specie;
    }
    public  void setSpecie(String specie){
        this.specie = specie;
    }

    private String name;

    public String getName(){
        return name;
    }
    public  void setName(String name){
        this.name = name;
    }

    private Integer age ;

    public Integer getAge(){
        return age;
    }
    public  void setAge(Integer age){
        this.age = age;
    }
}
